<script type="text/javascript">
	$(document).on("click","#show_number_follower_profile",function(){
		var member_id = "<?php echo @$member_id;?>";
		$.ajax({
			url:"<?php echo base_url("profile/get_follower_profile");?>",
			type:"post",
			dataType:"json",
			data:{member_id:member_id,items:0},
			success:function(res){
				if(res["status"] == "success"){
					if(res["show"] == false){
						$("#View_profile_follow #more-data").hide();
					}else{
						$("#View_profile_follow #more-data").show();
					}
					$("#View_profile_follow #number-view").text(res["number_view_profile"]);
					$("#View_profile_follow .modal-body .list").html(res["response"]);
					$("#View_profile_follow").modal();
				}else{
					alert("Error!");
				}
			},
			error:function(){
				alert("Error!");
			}

		});
		
		return false;
	});
	$(document).on("click","#View_profile_follow #more-data",function(){
		var items = $("#View_profile_follow .modal-body .list .media").length;
		var member_id = "<?php echo @$member_id;?>";
		$.ajax({
			url:"<?php echo base_url("profile/get_viewed_profile");?>",
			type:"post",
			dataType:"json",
			data:{member_id:member_id,items:items},
			success:function(res){
				if(res["status"] == "success"){
					if(res["show"] == false){
						$("#View_profile_follow #more-data").hide();
					}else{
						$("#View_profile_follow #more-data").show();
					}
					$("#View_profile_follow #number-view").text(res["number_view_profile"]);
					$("#View_profile_follow .modal-body .list").append(res["response"]);
				}else{
					alert("Error!");
				}
			},
			error:function(){
				alert("Error!");
			}
		});
		return false;
	});
</script>